from urllib.parse import urlparse
import requests

class Validator:
    def __init__(self, blockchain):
        self.blockchain = blockchain
        self.nodes = set()

    def register_node(self, address):
        """
        Add a new node to the list of nodes
        :param address: <str> Address of node. Eg. 'http://192.168.0.5:5000'
        :return: None
        """

        parsed_url = urlparse(address)
        if parsed_url.netloc:
            self.nodes.add(parsed_url.netloc)
        elif parsed_url.path:
            # Accepts an URL without scheme like '192.168.0.5:5000'.
            self.nodes.add(parsed_url.path)
        else:
            raise ValueError('Invalid URL')

    def verify_transaction(self, sender, recipient, amount):
        """
        Verify a transaction by checking whether the sender has sufficient funds.
        :param sender: <str> Address of the Sender
        :param recipient: <str> Address of the Recipient
        :param amount: <int> Amount
        :return: <bool> True if verified, False otherwise
        """

        if sender == '0':
            return True

        balance = self.get_balance(sender)
        return balance >= amount

    def verify_chain(self):
        """
        Verify the chain by checking whether the chain is valid and up to date
        :return: <bool> True if verified, False otherwise
        """

        current_index = 1
        current_block = self.blockchain.chain[0]
        while current_index < len(self.blockchain.chain):
            last_block = current_block
            current_block = self.blockchain.chain[current_index]
            if last_block['previous_hash'] != self.blockchain.hash(current_block):
                return False

            if not self.valid_proof(last_block['proof'], current_block['proof']):
                return False

            current_index += 1

        return True

    def valid_proof(self, last_proof, proof):
        """
        Validates the Proof: Does hash(last_proof, proof) contain 4 leading zeroes?
        :param last_proof: <int> Previous Proof
        :param proof: <int> Current Proof
        :return: <bool> True if correct, False if not.
        """

        guess = f'{last_proof}{proof}'.encode()
        guess_hash = hashlib.sha256(guess).hexdigest()
        return guess_hash[:4] == "0000"

    def resolve_conflicts(self):
        """
        This is our Consensus Algorithm, it resolves conflicts
        by replacing our chain with the longest one in the network.
        :return: <bool> True if our chain was replaced, False if not
        """

        neighbours = self.nodes
        new_chain = None

        # We're only looking for chains longer than ours
        max_length = len(self.blockchain.chain)

        # Grab and verify the chains from all the nodes in our network
        for node in neighbours:
            response = requests.get(f'http://{node}/chain')

            if response.status_code == 200:
                length = response.json()['length']
                chain = response.json()['chain']

                # Check if the length is longer and the chain is valid
                if length > max_length and self.blockchain.valid_chain(chain):
                    max_length = length
                    new_chain = chain

        # Replace our chain if we discovered a new, valid chain longer than ours
        if new_chain:
            self.blockchain.chain = new_chain
            return True

        return False

